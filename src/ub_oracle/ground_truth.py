"""Step 45 — labeled ground-truth set for defensible precision/recall.

A *large* (>= 500 pairs across >= 2 language pairs) corpus of self-contained
``(C program, target translation)`` items, where the ground-truth label —
``"divergent"`` or ``"equivalent"`` — is established **not by the oracle** but by
*bounded enumeration + real sanitizers*. This is the substrate that makes a
precision/recall claim defensible: the labels come from compiling and running the
programs under clang's UndefinedBehaviorSanitizer / bounds instrumentation and
comparing observable outputs, an authority that is completely independent of the
verifier under test.

Labeling rule (the authoritative ground truth), per item:

* Compile the C program with ``-fsanitize=undefined -fno-sanitize-recover=all``
  and run it on the item's (baked-in) input.

  - If the sanitizer **traps**, the C program executes undefined behavior on that
    input. We then compile and run the target twice; if the target is *defined*
    (a value or a guaranteed deterministic panic, per its semantics pack) and
    deterministic, the pair is a genuine **definedness divergence** →
    ``"divergent"``. (A trapping C with a non-defined target is an inconclusive
    item and is dropped from the labeled set.)

  - If the sanitizer does **not** trap, the C program is well-defined on that
    input. We compile and run both the (un-instrumented, ``-O0``) C and the
    target and compare their observable output. Equal, defined, deterministic
    output → ``"equivalent"``. Differing-but-both-defined output → ``"divergent"``
    (an *observable-value* divergence with no UB).

The corpus is generated by parametric families (``enumerate_corpus``) so it scales
past 500 items deterministically. Every item also carries a ``declared_label`` from
its construction; :func:`establish_ground_truth` runs the sanitizer labeler on a
sample (or all) and reports the agreement between *declared* and *observed* labels
— i.e. it proves the construction is faithful to the sanitizer reality, which is
exactly what a reviewer needs to trust the >=500-item label set.

Nothing here is asserted: labels are produced by executing real binaries.
"""

from __future__ import annotations

import hashlib
import os
import tempfile
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple

from .reexec import ReexecHarness, ToolchainStatus, toolchain_available

INT_MIN_C = "(-2147483647 - 1)"

# --------------------------------------------------------------------------- #
# A single labeled item. Operands are baked in as literals so the program is
# fully self-contained (no argv parsing, trivially deterministic).
# --------------------------------------------------------------------------- #
@dataclass(frozen=True)
class GTItem:
    item_id: str
    lang: str                 # "rust" | "go"
    klass: str                # family tag, e.g. "div_by_zero", "safe_add"
    cwe: str                  # "CWE-369" ... or "" for equivalent items
    declared_label: str       # "divergent" | "equivalent"
    c_src: str
    target_src: str
    inputs: Tuple[str, ...]   # baked-in operands → always ()

    @property
    def content_hash(self) -> str:
        h = hashlib.sha256()
        h.update(self.lang.encode())
        h.update(self.c_src.encode())
        h.update(self.target_src.encode())
        return h.hexdigest()[:16]


# --------------------------------------------------------------------------- #
# Source builders. Each returns the full C and target source for a given set of
# literal operands.
# --------------------------------------------------------------------------- #
def _c(body: str) -> str:
    return ("#include <stdio.h>\n#include <stdlib.h>\n"
            "int main(int argc, char **argv) {\n" + body + "\n  return 0;\n}\n")


def _rust(body: str) -> str:
    return "use std::env;\nfn main() {\n" + body + "\n}\n"


def _go(body: str) -> str:
    return ("package main\n\nimport (\n\t\"fmt\"\n\t\"os\"\n\t\"strconv\"\n)\n\n"
            "func main() {\n" + body + "\n}\n")


# Operands are read from argv at *runtime* so neither rustc's const-evaluator
# (whose deny-by-default `unconditional_panic` lint would otherwise reject e.g.
# `1 / 0` or `t[4]` at compile time) nor any backend can fold the undefined
# operation away before it actually executes.
def _c_geti(idx: int) -> str:
    return f"atoi(argv[{idx}])"


def _rust_geti(idx: int, ty: str = "i32") -> str:
    return f"env::args().nth({idx}).unwrap().parse::<{ty}>().unwrap()"


def _go_geti(idx: int) -> str:
    return f"func() int {{ v, _ := strconv.Atoi(os.Args[{idx}]); return v }}()"


# ---- divergent families --------------------------------------------------- #
def _div0(lang: str, a: int) -> Tuple[str, str]:
    c = _c(f'  int a = {_c_geti(1)};\n  int b = {_c_geti(2)};\n'
           f'  printf("%d\\n", a / b);')
    if lang == "rust":
        t = _rust(f'  let a: i32 = {_rust_geti(1)};\n  let b: i32 = {_rust_geti(2)};\n'
                  f'  println!("{{}}", a / b);')
    else:
        t = _go(f'\ta := {_go_geti(1)}\n\tb := {_go_geti(2)}\n\tfmt.Println(a / b)')
    return c, t


def _oob(lang: str, idx: int) -> Tuple[str, str]:
    c = _c(f'  int t[4] = {{10, 20, 30, 40}};\n  int i = {_c_geti(1)};\n'
           f'  printf("%d\\n", t[i]);')
    if lang == "rust":
        t = _rust(f'  let t = [10i32, 20, 30, 40];\n  let i: usize = {_rust_geti(1, "usize")};\n'
                  f'  println!("{{}}", t[i]);')
    else:
        t = _go(f'\tt := []int{{10, 20, 30, 40}}\n\ti := {_go_geti(1)}\n\tfmt.Println(t[i])')
    return c, t


def _overflow(lang: str, k: int) -> Tuple[str, str]:
    c = _c(f'  int a = {_c_geti(1)};\n  int b = {_c_geti(2)};\n'
           f'  printf("%d\\n", a + b);')
    # rust only (Go's int is 64-bit, so this would not overflow → not divergent)
    t = _rust(f'  let a: i32 = {_rust_geti(1)};\n  let b: i32 = {_rust_geti(2)};\n'
              f'  println!("{{}}", a.wrapping_add(b));')
    return c, t


def _oversized_shift(lang: str, s: int) -> Tuple[str, str]:
    c = _c(f'  int x = {_c_geti(1)};\n  int s = {_c_geti(2)};\n'
           f'  printf("%d\\n", x << s);')
    if lang == "rust":
        t = _rust(f'  let x: i32 = {_rust_geti(1)};\n  let s: u32 = {_rust_geti(2, "u32")};\n'
                  f'  println!("{{}}", x.wrapping_shl(s));')
    else:
        # Go int is 64-bit; a shift by 32..60 is in-range and defined there,
        # while the C int shift by >= 32 is undefined → trap-vs-defined.
        t = _go(f'\tx := {_go_geti(1)}\n\ts := uint({_go_geti(2)})\n\tfmt.Println(x << s)')
    return c, t


def _intmin(lang: str) -> Tuple[str, str]:
    c = _c(f'  int a = {_c_geti(1)};\n  int b = {_c_geti(2)};\n'
           f'  printf("%d\\n", a / b);')
    t = _rust(f'  let a: i32 = {_rust_geti(1)};\n  let b: i32 = {_rust_geti(2)};\n'
              f'  println!("{{}}", a.wrapping_div(b));')
    return c, t


