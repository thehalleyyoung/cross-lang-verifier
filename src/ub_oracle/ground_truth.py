# --------------------------------------------------------------------------- #
# Parametric enumeration → the >= 500-item labeled corpus.
# --------------------------------------------------------------------------- #
def _divergent_items(lang: str) -> List[GTItem]:
    items: List[GTItem] = []
    # division by zero — vary the (irrelevant) numerator to get distinct programs
    for a in range(1, 41):
        c, t = _div0(lang, a)
        items.append(GTItem(f"{lang}-div0-{a}", lang, "div_by_zero", "CWE-369",
                            "divergent", c, t, (str(a), "0")))
    # out-of-bounds read — index past the length-4 array
    for i in range(4, 40):
        c, t = _oob(lang, i)
        items.append(GTItem(f"{lang}-oob-{i}", lang, "oob_read", "CWE-125",
                            "divergent", c, t, (str(i),)))
    # shift past bit width — undefined in C (int is 32-bit), defined in target
    for s in range(32, 61):
        c, t = _oversized_shift(lang, s)
        items.append(GTItem(f"{lang}-shift-{s}", lang, "oversized_shift", "CWE-758",
                            "divergent", c, t, ("1", str(s))))
    if lang == "rust":
        # signed overflow (32-bit only meaningful where target int is 32-bit)
        for k in range(1, 41):
            c, t = _overflow(lang, k)
            items.append(GTItem(f"{lang}-ovf-{k}", lang, "signed_overflow", "CWE-190",
                                "divergent", c, t, ("2147483647", str(k))))
        c, t = _intmin(lang)
        items.append(GTItem(f"{lang}-intmin", lang, "int_min_div_neg1", "CWE-682",
                            "divergent", c, t, ("-2147483648", "-1")))
    return items


def _equivalent_items(lang: str) -> List[GTItem]:
    items: List[GTItem] = []
    # safe addition — small in-range operands, identical defined output
    pairs = [(a, b) for a in range(1, 9) for b in range(1, 6)]
    for n, (a, b) in enumerate(pairs):
        c, t = _safe_add(lang, a, b)
        items.append(GTItem(f"{lang}-add-{n}", lang, "safe_add", "",
                            "equivalent", c, t, (str(a), str(b))))
    # safe multiplication
    mpairs = [(a, b) for a in range(2, 8) for b in range(2, 7)]
    for n, (a, b) in enumerate(mpairs):
        c, t = _safe_mul(lang, a, b)
        items.append(GTItem(f"{lang}-mul-{n}", lang, "safe_mul", "",
                            "equivalent", c, t, (str(a), str(b))))
    # safe in-bounds index
    for i in range(0, 8):
        for rep in range(5):
            c, t = _safe_index(lang, i)
            # distinct program by appending a harmless distinct comment
            c2 = c + f"/*v{rep}*/"
            items.append(GTItem(f"{lang}-idx-{i}-{rep}", lang, "safe_index", "",
                                "equivalent", c2, t, (str(i),)))
    # safe shift within range
    spairs = [(x, s) for x in range(1, 6) for s in range(0, 6)]
    for n, (x, s) in enumerate(spairs):
        c, t = _safe_shift(lang, x, s)
        items.append(GTItem(f"{lang}-sshift-{n}", lang, "safe_shift", "",
                            "equivalent", c, t, (str(x), str(s))))
    # safe modulo
    modpairs = [(a, b) for a in range(10, 40, 3) for b in range(2, 7)]
    for n, (a, b) in enumerate(modpairs):
        c, t = _safe_mod(lang, a, b)
        items.append(GTItem(f"{lang}-mod-{n}", lang, "safe_mod", "",
                            "equivalent", c, t, (str(a), str(b))))
    return items


def enumerate_corpus(langs: Tuple[str, ...] = ("rust", "go")) -> List[GTItem]:
    """The full deterministically-enumerated labeled corpus (>= 500 items)."""
    items: List[GTItem] = []
    for lang in langs:
        items.extend(_divergent_items(lang))
        items.extend(_equivalent_items(lang))
    return items


# --------------------------------------------------------------------------- #
# The authoritative sanitizer-based labeler.
# --------------------------------------------------------------------------- #
@dataclass
class LabelEvidence:
    observed_label: str       # "divergent" | "equivalent" | "inconclusive" | "uncompilable"
    ub_trapped: bool
    c_out: str
    target_out: str
    detail: str


def label_item(h: ReexecHarness, item: GTItem) -> LabelEvidence:
    """Establish the *ground-truth* label for one item by compiling and running
    it under sanitizers — independent of any oracle verdict."""
    argv = list(item.inputs)
    with tempfile.TemporaryDirectory() as d:
        san = h._compile_c(
            item.c_src,
            ["-O1", "-fsanitize=undefined", "-fno-sanitize-recover=all"],
            d, "san")
        o0 = h._compile_c(item.c_src, ["-O0"], d, "o0")
        tgt = h._compile_target(item.target_src, item.lang, d, "tgt")
        if not all((san, o0, tgt)):
            return LabelEvidence("uncompilable", False, "", "",
                                 f"compile san={bool(san)} o0={bool(o0)} tgt={bool(tgt)}")
        san_run = h._run([san, *argv])
        c_run = h._run([o0, *argv])
        t_a = h._run([tgt, *argv])
        t_b = h._run([tgt, *argv])

    tgt_det = (t_a.returncode == t_b.returncode and t_a.stdout == t_b.stdout)
    tgt_defined = (t_a.target_outcome_defined(item.lang)
                   and t_b.target_outcome_defined(item.lang) and tgt_det)

    if san_run.ub_trapped:
        if tgt_defined:
            return LabelEvidence("divergent", True, "", t_a.stdout,
                                 f"C UB-trapped ({san_run.ub_category!r}); "
                                 f"target defined rc={t_a.returncode}")
        return LabelEvidence("inconclusive", True, "", t_a.stdout,
                             "C UB-trapped but target not defined/deterministic")

    # C is well-defined on this input.
    c_defined = (c_run.returncode == 0)
    if c_defined and tgt_defined and t_a.returncode == 0:
        if c_run.stdout == t_a.stdout:
            return LabelEvidence("equivalent", False, c_run.stdout, t_a.stdout,
                                 "both defined, identical output")
        return LabelEvidence("divergent", False, c_run.stdout, t_a.stdout,
                             "both defined, differing output (value divergence)")
    return LabelEvidence("inconclusive", False, c_run.stdout, t_a.stdout,
                         f"c_rc={c_run.returncode} tgt_defined={tgt_defined}")


# --------------------------------------------------------------------------- #
# Establishing & confirming the ground truth.
# --------------------------------------------------------------------------- #
@dataclass
class GTResult:
    item_id: str
    lang: str
    klass: str
    declared_label: str
    observed_label: str
    agrees: bool
    detail: str


@dataclass
class GTReport:
    available: bool
    total_items: int
    langs: Tuple[str, ...]
    results: List[GTResult] = field(default_factory=list)

    @property
    def labeled(self) -> List[GTResult]:
        return [r for r in self.results
                if r.observed_label in ("divergent", "equivalent")]

    @property
    def agreements(self) -> int:
        return sum(1 for r in self.results if r.agrees)

    @property
    def disagreements(self) -> List[GTResult]:
        return [r for r in self.results
                if r.observed_label in ("divergent", "equivalent") and not r.agrees]

    @property
    def ok(self) -> bool:
        # Every sanitizer-labeled item must match its declared label, and at
        # least one item of each label must have been observed.
        lab = self.labeled
        if not lab:
            return False
        if self.disagreements:
            return False
        kinds = {r.observed_label for r in lab}
        return {"divergent", "equivalent"}.issubset(kinds)


def _sample_indices(items: List[GTItem], per_class: int) -> List[int]:
    """A deterministic spread: the first ``per_class`` items of every
    (lang, klass) bucket — covering every family, both languages, both labels."""
    seen: Dict[Tuple[str, str], int] = {}
    picked: List[int] = []
    for idx, it in enumerate(items):
        key = (it.lang, it.klass)
        n = seen.get(key, 0)
        if n < per_class:
            picked.append(idx)
            seen[key] = n + 1
    return picked


def establish_ground_truth(langs: Tuple[str, ...] = ("rust", "go"),
                           status: Optional[ToolchainStatus] = None,
                           sample_per_class: Optional[int] = 1) -> GTReport:
    """Run the sanitizer labeler over the corpus (or a per-family sample) and
    compare the authoritative observed label against the declared label."""
    st = status or toolchain_available()
    items = enumerate_corpus(langs)
    avail_langs = tuple(l for l in langs if st.full_for(l))
    rep = GTReport(available=bool(avail_langs), total_items=len(items),
                   langs=avail_langs)
    if not avail_langs:
        return rep
    items = [it for it in items if it.lang in avail_langs]
    h = ReexecHarness(st)
    if sample_per_class is None:
        chosen = list(range(len(items)))
    else:
        chosen = _sample_indices(items, sample_per_class)
    for idx in chosen:
        it = items[idx]
        ev = label_item(h, it)
        rep.results.append(GTResult(
            it.item_id, it.lang, it.klass, it.declared_label,
            ev.observed_label, ev.observed_label == it.declared_label, ev.detail))
    return rep


@dataclass
class GTConfirmation:
    available: bool
    ok: bool
    corpus_size: int
    n_langs: int
    report: GTReport


def confirm_ground_truth(langs: Tuple[str, ...] = ("rust", "go"),
                         sample_per_class: Optional[int] = 1) -> GTConfirmation:
    """Confirm the labeled set is (a) large enough (>=500 across >=2 pairs) and
    (b) faithfully labeled — every sampled sanitizer-observed label matches the
    declared label."""
    full = enumerate_corpus(langs)
    rep = establish_ground_truth(langs, sample_per_class=sample_per_class)
    n_langs = len({it.lang for it in full})
    size_ok = len(full) >= 500 and n_langs >= 2
    ok = bool(rep.available) and rep.ok and size_ok
    return GTConfirmation(available=bool(rep.available), ok=ok,
                          corpus_size=len(full), n_langs=n_langs, report=rep)


def corpus_stats(langs: Tuple[str, ...] = ("rust", "go")) -> Dict[str, object]:
    """Class/label/lang distribution of the labeled corpus (for the paper's
    'ground-truth set' table)."""
    items = enumerate_corpus(langs)
    by_label: Dict[str, int] = {}
    by_lang: Dict[str, int] = {}
    by_class: Dict[str, int] = {}
    for it in items:
        by_label[it.declared_label] = by_label.get(it.declared_label, 0) + 1
        by_lang[it.lang] = by_lang.get(it.lang, 0) + 1
        by_class[it.klass] = by_class.get(it.klass, 0) + 1
    return {
        "total": len(items),
        "n_langs": len(by_lang),
        "by_label": by_label,
        "by_lang": by_lang,
        "by_class": by_class,
    }


